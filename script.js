document.getElementById("chapter-btn").addEventListener("click", () => {
  // Get the selected chapter value
  const selectedChapter = chapterSelect.value;

  // If the user hasn't selected a chapter, display an error message and return
  if (selectedChapter === "") {
    chapterInfoContainer.innerHTML = "<p>Please select a chapter.</p>";
    return;
  }

  // Get the information for the selected chapter from the chapters object
  const chapterTitle = chapters[selectedChapter].title;
  const chapterInfo = chapters[selectedChapter].info;

  // Display the chapter information in the container
  document.getElementById("chapter-title").textContent = chapterTitle;
  document.getElementById("chapter-info").textContent = chapterInfo;
});
